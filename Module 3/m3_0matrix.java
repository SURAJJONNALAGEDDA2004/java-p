public class m3_0matrix {
    

public static void main(String ar[])
{
	int mat[][]=new int[3][4];
	for(int i=0;i<3;i++)
	{
		for(int j=0;j<4;j++)
		{
			mat[i][j]=i; 
			System.out.print(mat[i][j]+" ");
}
System.out.println();
	}
}
}

